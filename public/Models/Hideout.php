    private ?String $code = null;
    private ?String $address = null;
    private ?String $country = null;
    private ?String $type = null;

    /**
     * Get the value of id
     */ 
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set the value of id
     *
     * @return  self
     */ 
